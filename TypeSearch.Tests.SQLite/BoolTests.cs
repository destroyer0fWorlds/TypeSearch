        TestContext GetTestContext()
        {
            var options = new DbContextOptionsBuilder()
                .UseSqlite()
                .Options;

            var dbName = $"TypeSearch_UnitTests_SQLite_{nameof(BoolTests)}";
            var db = new TestContext(options, dbName);

            db.Database.EnsureCreated();

            // Ensure the db has records in it before attempting to search
            var testEntity = db.TestEntities.FirstOrDefault();
            if (testEntity == null)
            {
                db.TestEntities.Add(new TestEntity() { BoolProperty = true });
                db.TestEntities.Add(new TestEntity() { BoolProperty = false });
                db.TestEntities.Add(new TestEntity() { BoolProperty = true });
                db.TestEntities.Add(new TestEntity() { BoolProperty = false });
                db.TestEntities.Add(new TestEntity() { BoolProperty = true });
                db.SaveChanges();
            }

            return db;
        }
